a = float(input("Введите коеффициент a: "))
b = float(input("Введите коеффициент b: "))
c = float(input("Введите коеффициент c: "))
D = b**2 - (4 * a * c)
x1 = (-b + D**0.5) / (2 * a)
x2 = (-b - D**0.5) / (2 * a)
if D == 0:
    print("Ответ:", "x1 =", x1, "x2 =", x1)
elif D > 0:
    print("Ответ:", "x1 =", x1, ";", "x2 =", x2)
else:
    print("Ответ:", "Пустое множество")